static void lcm_init(void)
{
	int tp_rst_pin;
	int ret = 0;

	/* keep tp reset high */
	tp_rst_pin = lcm_util_get_pin("tp_rst_out1_gpio");

	if (tp_rst_pin < 0) {
		LCM_LOGI("rp_rst_pin not defined\n");
		return;
	}
	tp_rst_pin |= PIN_ENCRYPT;
	mt_set_gpio_out(tp_rst_pin, GPIO_OUT_ZERO);
	MDELAY(5);
	mt_set_gpio_out(tp_rst_pin, GPIO_OUT_ONE);

#ifndef MACH_FPGA
	/* config rt5081 register 0xB2[7:6]=0x3, that is set db_delay=4ms.*/
	ret = PMU_REG_MASK(0xB2, (0x3 << 6), (0x3 << 6));

	/* set AVDD 5.5v, (4v+30*0.05v) */
	/* ret = RT5081_write_byte(0xB3, (1 << 6) | 28);*/
	ret = PMU_REG_MASK(0xB3, 30, (0x3F << 0));
	if (ret < 0)
		LCM_LOGI("td4320_fhdp----tps6132----cmd=%0x--i2c write error----\n", 0xB3);
	else
		LCM_LOGI("td4320_fhdp----tps6132----cmd=%0x--i2c write success----\n", 0xB3);

	/* set AVEE */
	/* ret = RT5081_write_byte(0xB4, (1 << 6) | 28); */
	ret = PMU_REG_MASK(0xB4, 30, (0x3F << 0));
	if (ret < 0)
		LCM_LOGI("td4320_fhdp----tps6132----cmd=%0x--i2c write error----\n", 0xB4);
	else
		LCM_LOGI("td4320_fhdp----tps6132----cmd=%0x--i2c write success----\n", 0xB4);

	/* enable AVDD & AVEE */
	/* 0x12--default value; bit3--Vneg; bit6--Vpos; */
	/* ret = RT5081_write_byte(0xB1, 0x12 | (1<<3) | (1<<6)); */
	ret = PMU_REG_MASK(0xB1, (1<<3) | (1<<6), (1<<3) | (1<<6));
	if (ret < 0)
		LCM_LOGI("td4320_fhdp----tps6132----cmd=%0x--i2c write error----\n", 0xB1);
	else
		LCM_LOGI("td4320_fhdp----tps6132----cmd=%0x--i2c write success----\n", 0xB1);

	MDELAY(15);
#endif
	SET_RESET_PIN(1);
	MDELAY(1);
	SET_RESET_PIN(0);
	MDELAY(10);

	SET_RESET_PIN(1);
	MDELAY(5);

	push_table(init_setting_vdo, sizeof(init_setting_vdo) /
		   sizeof(struct LCM_setting_table), 1);

#ifdef LCM_SET_DISPLAY_ON_DELAY
	lcm_init_tick = gpt4_get_current_tick();
	is_display_on = 0;
#endif
}

#ifdef LCM_SET_DISPLAY_ON_DELAY
static int lcm_set_display_on(void)
{
	U32 timeout_tick, i = 0;

	if (is_display_on)
		return 0;

	/* we need to wait 150ms after lcm init to set display on */
	timeout_tick = gpt4_time2tick_ms(150);

	while (!gpt4_timeout_tick(lcm_init_tick, timeout_tick)) {
		i++;
		if (i % 1000 == 0) {
			LCM_LOGI("td4320 %s error: i=%u,lcm_init_tick=%u,cur_tick=%u\n",
				 __func__, i, lcm_init_tick, gpt4_get_current_tick());
		}
	}

	push_table(set_display_on, sizeof(set_display_on) /
		   sizeof(struct LCM_setting_table), 1);

	is_display_on = 1;
	return 0;
}
#endif

static void lcm_suspend(void)
{
	int ret = 0;
	push_table(lcm_suspend_setting, sizeof(lcm_suspend_setting) /
		   sizeof(struct LCM_setting_table), 1);
#ifndef MACH_FPGA
	/* enable AVDD & AVEE */
	/* 0x12--default value; bit3--Vneg; bit6--Vpos; */
	/* ret = RT5081_write_byte(0xB1, 0x12); */
	ret = PMU_REG_MASK(0xB1, (0<<3) | (0<<6), (1<<3) | (1<<6));
	if (ret < 0)
		LCM_LOGI("td4320_fhdp----tps6132----cmd=%0x--i2c write error----\n", 0xB1);
	else
		LCM_LOGI("td4320_fhdp----tps6132----cmd=%0x--i2c write success----\n", 0xB1);

	MDELAY(5);
#endif

	SET_RESET_PIN(0);
	MDELAY(10);
}

