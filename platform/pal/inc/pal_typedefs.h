#ifndef __PAL_TYPEDEFS_H__
#define __PAL_TYPEDEFS_H__
#include <sys/types.h>

#define PAL_UNUSED_PARAM(param) ((void)param)

#ifndef FALSE
#define FALSE   0
#endif

#ifndef TRUE
#define TRUE    1
#endif

#ifndef NULL
#define NULL    0
#endif

#ifndef BOOL
typedef unsigned char BOOL;
#endif

typedef signed char        int8_t;
typedef signed short       int16_t;
typedef signed int         int32_t;
typedef unsigned char      uint8_t;
typedef unsigned short     uint16_t;
typedef unsigned int       uint32_t;
typedef unsigned long long uint64_t;
typedef long               ssize_t;

typedef volatile uint8_t  *P_U8;
typedef volatile uint16_t *P_U16;
typedef volatile uint32_t *P_U32;

#endif /* __PAL_TYPEDEFS_H__ */
