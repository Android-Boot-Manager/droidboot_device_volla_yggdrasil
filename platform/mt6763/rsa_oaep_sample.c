#define TST_MSG_SZ  (16)

static void dump_buf(unsigned char *buf, unsigned int buf_sz)
{
	unsigned int idx = 0;

	if (buf_sz < 4)
		return;

	for (idx = 0; idx <= buf_sz - 4; idx += 4) {
		dprintf(CRITICAL, "[0x%x] 0x%x 0x%x 0x%x 0x%x\n", idx,
								*(buf + idx),
								*(buf + idx + 1),
								*(buf + idx + 2),
								*(buf + idx + 3));
	}

	return;
}

void rsa_oaep_sample()
{
	unsigned int ret = 0;
	unsigned int idx = 0;

	unsigned char key_n[] = {RSA_TST_KEY_N};
	unsigned char key_e[] = {RSA_TST_KEY_E};
	unsigned char key_d[] = {RSA_TST_KEY_D};

	unsigned int key_n_sz = sizeof(key_n);
	unsigned int key_e_sz = sizeof(key_e);
	unsigned int key_d_sz = sizeof(key_d);

	unsigned char msg[TST_MSG_SZ] = {0};
	unsigned char emsg[RSA_MODULUS_SZ] = {0};
	unsigned int msg_sz = TST_MSG_SZ;
	unsigned int emsg_sz = RSA_MODULUS_SZ;

	for (idx = 0; idx < TST_MSG_SZ; idx++) {
		msg[idx] = idx % 16;
	}

	ret = rsa_oaep_encrypt(msg, msg_sz, key_e, key_e_sz, key_n, key_n_sz, emsg, &emsg_sz);
	if (ret) {
		dprintf(CRITICAL, "rsa_oaep_encrypt test fail(0x%x)\n", ret);
		return;
	}

	ret = rsa_oaep_decrypt(emsg, emsg_sz, key_d, key_d_sz, key_n, key_n_sz, msg, &msg_sz);
	if (ret) {
		dprintf(CRITICAL, "rsa_oaep_decrypt test fail(0x%x)\n", ret);
		return;
	}

	for (idx = 0; idx < TST_MSG_SZ; idx++) {
		if (msg[idx] != (idx % 16)) {
			dprintf(CRITICAL, "rsa_oaep test failed\n");
			return;
		}
	}

	dprintf(CRITICAL, "rsa_oaep test pass\n");

	return;
}

